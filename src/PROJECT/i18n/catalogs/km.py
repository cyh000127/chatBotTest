def format_today_date(current: datetime) -> str:
    weekdays = ["ច", "អ", "ព", "ព្រហ", "សុ", "សៅ", "អាទិ"]
    weekday = weekdays[current.weekday()]
    return (
        "ថ្ងៃនេះ។\n"
        f"- កាលបរិច្ឆេទ: {current.strftime('%Y-%m-%d')} ({weekday})\n"
        f"- ម៉ោង: {current.strftime('%H:%M:%S')} KST"
    )


