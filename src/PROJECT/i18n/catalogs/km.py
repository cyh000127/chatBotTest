def format_today_date(current: datetime) -> str:
    weekdays = ["бһ…", "бһў", "бһ–", "бһ–бҹ’бһҡ", "бһҹбһ»", "бһҹ", "бһўбһ¶"]
    weekday = weekdays[current.weekday()]
    return (
        "бһҖбһ¶бһӣбһ”бһҡбһ·бһ…бҹ’бһҶбҹҒбһ‘бһҗбҹ’бһ„бҹғбһ“бҹҒбҹҮбҹ”\n"
        f"- бһҖбһ¶бһӣбһ”бһҡбһ·бһ…бҹ’бһҶбҹҒбһ‘: {current.strftime('%Y-%m-%d')} ({weekday})\n"
        f"- бһҳбҹүбҹ„бһ„: {current.strftime('%H:%M:%S')} KST"
    )


